// 编写一个C程序，提示用户输入两个整数，然后判断两个整数的大小关系，并输出对应的提示信息。
// 如果第一个整数大于第二个整数，输出"第一个数大于第二个数"；如果第一个数小于第二个数，输出"第一个数小于第二个数"；
// 如果两个数相等，输出"两个数相等"。

#include <stdio.h>

int main() {
    int num1, num2; // 声明两个整型变量num1和num2，用于存储用户输入的整数值
    
    printf("请输入两个整数，用空格隔开：");
    scanf("%d %d", &num1, &num2); // 提示用户输入两个整数，并读取用户输入的值分别存储到变量num1和num2中

    if (num1 > num2) { // 如果第一个整数大于第二个整数
        printf("第一个数大于第二个数\n"); // 输出提示信息
    } else if (num1 < num2) { // 如果第一个数小于第二个数
        printf("第一个数小于第二个数\n"); // 输出提示信息
    } else { // 如果两个数相等
        printf("两个数相等\n"); // 输出提示信息
    }
    
    return 0;
}